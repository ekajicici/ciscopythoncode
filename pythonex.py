# area of a field
width = float(input("enter the width:"))
length = float(input("enter the length:"))
print("width is =", width, "Ft")
print("length is =", length, "Ft")
area1 = width * length
print("THE AREA OF THE FIELD IN FEET IS =", area1, "Ft")
area2 = (width * length) / 43560
print("THE AREA OF THE FIELD IN ACRES IS =", area2, "acres")
